#!/usr/bin/env node
/**
 * FitJourney — Deploy Cache Busting Routine
 *
 * Run AFTER `vite build` and BEFORE uploading `dist/` to the CDN.
 *
 * Steps:
 *  1. Generate a fresh build hash + timestamp into dist/version.json
 *  2. Inject the same hash into index.html as <meta name="build-hash">
 *  3. Write a CDN-ready _headers file (Netlify/Cloudflare Pages) so:
 *       - hashed assets (/assets/*) are immutable
 *       - index.html / version.json / sw*.js are NEVER cached
 *  4. Emit a kill-switch service worker (`sw-kill.js`) that any old SW
 *     registration will fetch on update — and immediately self-unregister.
 *
 * Usage:
 *   node scripts/deploy-cache-bust.mjs
 *   # then upload ./dist to your host (Vercel / Netlify / Cloudflare / S3)
 */
import { writeFileSync, readFileSync, existsSync, mkdirSync } from "node:fs";
import { join } from "node:path";
import { randomBytes } from "node:crypto";

const DIST = process.env.DIST_DIR || "dist";
if (!existsSync(DIST)) {
  console.error(`[deploy-cache-bust] ❌ "${DIST}/" not found. Run \`vite build\` first.`);
  process.exit(1);
}

// ── 1. Build identity ────────────────────────────────────────────────
const hash = randomBytes(6).toString("hex");
const timestamp = new Date().toISOString();
const version = `${hash}-${Date.now()}`;

const versionPayload = { version, hash, timestamp };
writeFileSync(join(DIST, "version.json"), JSON.stringify(versionPayload, null, 2));
console.log(`[deploy-cache-bust] ✅ version.json → ${version}`);

// ── 2. Stamp index.html ──────────────────────────────────────────────
const indexPath = join(DIST, "index.html");
if (existsSync(indexPath)) {
  let html = readFileSync(indexPath, "utf8");
  const meta = `<meta name="build-hash" content="${hash}"><meta name="build-time" content="${timestamp}">`;
  html = html.replace(/<meta name="build-hash"[^>]*>\s*<meta name="build-time"[^>]*>/g, "");
  html = html.replace(/<head>/i, `<head>\n  ${meta}`);
  writeFileSync(indexPath, html);
  console.log("[deploy-cache-bust] ✅ index.html stamped");
}

// ── 3. CDN cache headers (Netlify / Cloudflare Pages syntax) ─────────
const headers = `# Auto-generated by deploy-cache-bust.mjs

# Hashed build assets — immutable forever
/assets/*
  Cache-Control: public, max-age=31536000, immutable

# HTML shell — never cache
/
  Cache-Control: no-store, no-cache, must-revalidate
/index.html
  Cache-Control: no-store, no-cache, must-revalidate

# Version manifest — always fresh
/version.json
  Cache-Control: no-store, no-cache, must-revalidate

# Service workers — never cache (browser spec recommends max-age=0)
/sw.js
  Cache-Control: no-store, no-cache, must-revalidate
/sw-push.js
  Cache-Control: no-store, no-cache, must-revalidate
/sw-kill.js
  Cache-Control: no-store, no-cache, must-revalidate
`;
writeFileSync(join(DIST, "_headers"), headers);
console.log("[deploy-cache-bust] ✅ _headers written");

// ── 4. Kill-switch SW (replaces any stale SW with a self-uninstaller) ─
const killSw = `// Auto-generated kill-switch SW (build ${hash})
// Any browser holding an old SW will fetch this on update and unregister itself.
self.addEventListener("install", (e) => self.skipWaiting());
self.addEventListener("activate", (e) => {
  e.waitUntil((async () => {
    try {
      const keys = await caches.keys();
      await Promise.all(keys.map((k) => caches.delete(k)));
    } catch {}
    try {
      await self.registration.unregister();
      const clients = await self.clients.matchAll({ type: "window" });
      clients.forEach((c) => c.navigate(c.url));
    } catch {}
  })());
});
`;
writeFileSync(join(DIST, "sw-kill.js"), killSw);
console.log("[deploy-cache-bust] ✅ sw-kill.js emitted");

console.log("[deploy-cache-bust] 🚀 Done. Upload ./" + DIST + " to your host.");
