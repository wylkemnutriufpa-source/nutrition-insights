import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

Deno.serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const authHeader = req.headers.get("Authorization") || req.headers.get("authorization");

    if (!authHeader?.startsWith("Bearer ")) {
      return new Response(JSON.stringify({ error: "Unauthorized" }), {
        status: 401,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const supabaseUrl = Deno.env.get("SUPABASE_URL")!;
    const serviceKey = Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")!;

    const adminClient = createClient(supabaseUrl, serviceKey);
    const token = authHeader.replace("Bearer ", "").trim();
    const { data: { user }, error: userError } = await adminClient.auth.getUser(token);

    if (userError || !user) {
      console.error("Auth error:", userError?.message || "No user found");
      return new Response(JSON.stringify({ error: "Invalid token" }), {
        status: 401,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const userId = user.id;
    const userEmail = user.email || "unknown";

    const { data: roles, error: rolesError } = await adminClient
      .from("user_roles")
      .select("role")
      .eq("user_id", userId);

    if (rolesError) {
      console.error("Role lookup error:", rolesError.message);
      return new Response(JSON.stringify({ error: "Erro ao validar permissões" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const userRoles = (roles || []).map((roleRow: { role: string }) => roleRow.role);
    if (!userRoles.includes("admin") && !userRoles.includes("nutritionist")) {
      return new Response(JSON.stringify({ error: "Forbidden" }), {
        status: 403,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    const lines: string[] = [];
    lines.push("-- =============================================");
    lines.push("-- FitJourney SQL Backup");
    lines.push(`-- Generated at: ${new Date().toISOString()}`);
    lines.push(`-- Generated by: ${userEmail}`);
    lines.push("-- =============================================\n");

    const { data: tables, error: rpcError } = await adminClient.rpc("get_backup_tables");

    if (rpcError) {
      console.error("RPC error:", rpcError.message);
      return new Response(JSON.stringify({ error: `Erro ao buscar tabelas: ${rpcError.message}` }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    if (!tables || tables.length === 0) {
      return new Response(JSON.stringify({ error: "Nenhuma tabela encontrada para backup." }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    for (const tableInfo of tables) {
      const tableName = tableInfo.table_name;
      const createStmt = tableInfo.create_statement;

      lines.push(`-- ─── Table: ${tableName} ───`);
      lines.push(`DROP TABLE IF EXISTS public."${tableName}" CASCADE;`);
      lines.push(`${createStmt};\n`);

      const { data: rows, error: dataErr } = await adminClient.from(tableName).select("*").limit(50000);

      if (dataErr || !rows || rows.length === 0) {
        lines.push(`-- No data in ${tableName}\n`);
        continue;
      }

      const columns = Object.keys(rows[0]);
      const colList = columns.map((column) => `"${column}"`).join(", ");

      for (let i = 0; i < rows.length; i += 100) {
        const batch = rows.slice(i, i + 100);
        lines.push(`INSERT INTO public."${tableName}" (${colList}) VALUES`);
        const valueLines = batch.map((row) => {
          const values = columns.map((column) => {
            const value = row[column];
            if (value === null || value === undefined) return "NULL";
            if (typeof value === "number") return String(value);
            if (typeof value === "boolean") return value ? "TRUE" : "FALSE";
            if (typeof value === "object") {
              return `'${JSON.stringify(value).replace(/'/g, "''")}'::jsonb`;
            }
            return `'${String(value).replace(/'/g, "''")}'`;
          });

          return `  (${values.join(", ")})`;
        });

        lines.push(`${valueLines.join(",\n")};\n`);
      }
    }

    lines.push("-- ═══ End of backup ═══");

    return new Response(lines.join("\n"), {
      headers: {
        ...corsHeaders,
        "Content-Type": "application/sql",
        "Content-Disposition": `attachment; filename="fitjourney_backup_${new Date().toISOString().slice(0, 10)}.sql"`,
      },
    });
  } catch (error) {
    console.error("Backup error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Erro interno" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
